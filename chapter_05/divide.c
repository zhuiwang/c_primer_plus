#include <stdio.h>
int main(int argc, const char *argv[])
{
    printf("integer division 5/4    is %d  \n", 5/4);
    printf("integer division 5/4    is %d  \n", 5/4);
    printf("integer division 5/4    is %d  \n", 5/4);
    printf("floating division 5./4    is %1.2f  \n", 5./4);
    printf("mixed division 5/4.    is %1.2f  \n", 5/4.);
    return 0;
}
